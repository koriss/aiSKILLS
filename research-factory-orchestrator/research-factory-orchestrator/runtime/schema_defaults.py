@functools.cache
def _load_schema(schema_name: str) -> dict[str, Any] | None:
    path = _SCHEMAS_DIR / f"{schema_name}.schema.json"
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return None


def _synth_value(sub: dict[str, Any], root: dict[str, Any]) -> Any:
    if "const" in sub:
        return sub["const"]
    if "enum" in sub and isinstance(sub["enum"], list) and sub["enum"]:
        return sub["enum"][0]
    t = sub.get("type")
    if t == "string":
        return ""
    if t == "integer":
        return 0
    if t == "number":
        return 0
    if t == "boolean":
        return False
    if t == "array":
        return []
    if t == "object" or ("properties" in sub and isinstance(sub.get("properties"), dict)):
        return _synth_object(sub, root)
    if "oneOf" in sub and isinstance(sub.get("oneOf"), list):
        for br in sub["oneOf"]:
            if isinstance(br, dict):
                return _synth_value(br, root)
    if "anyOf" in sub and isinstance(sub.get("anyOf"), list):
        for br in sub["anyOf"]:
            if isinstance(br, dict):
                return _synth_value(br, root)
    return None


def _synth_object(schema: dict[str, Any], root: dict[str, Any]) -> dict[str, Any]:
    out: dict[str, Any] = {}
    props = schema.get("properties") if isinstance(schema.get("properties"), dict) else {}
    req = schema.get("required") if isinstance(schema.get("required"), list) else []
    for rk in req:
        if rk not in props or not isinstance(props[rk], dict):
            continue
        out[rk] = _synth_value(props[rk], root)
    return out


def _synth_from_schema(schema: dict[str, Any]) -> dict[str, Any]:
    root = schema
    if schema.get("type") == "object" or isinstance(schema.get("properties"), dict):
        return _synth_object(schema, root)
    return {}


def minimal_valid(schema_name: str, overrides: Mapping[str, Any] | None = None) -> dict[str, Any]:
    """Return a minimal object valid under v19 core schemas for rollback / worker bootstrap.

    ``schema_name`` is a basename key (e.g. ``delivery-manifest``), not a filesystem path.
    Unknown names raise ``RuntimeError`` (fail-closed).
    """
    if schema_name not in _EMERGENCY_DEFAULTS:
        raise RuntimeError(
            f"minimal_valid: unknown schema_name={schema_name!r}; add to _EMERGENCY_DEFAULTS whitelist"
        )
    schema = _load_schema(schema_name)
    base = copy.deepcopy(_EMERGENCY_DEFAULTS[schema_name])
    if schema is not None:
        try:
            synth = _synth_from_schema(schema)
            for k, v in synth.items():
                if k not in base or base[k] in (None, "", [], {}):
                    base[k] = v
        except Exception:
            pass
    if overrides:
        base.update(dict(overrides))
    if "created_at" in base and not base.get("created_at"):
        base["created_at"] = now()
    return base


def merge_rollback_delivery_manifest(dm: dict[str, Any]) -> dict[str, Any]:
    """Return a copy of delivery-manifest with validation-failed rollback fields (schema-safe)."""
    out: dict[str, Any] = dict(dm)
    out.update(
        {
            "delivery_status": "validation_failed",
            "real_external_delivery": False,
            "artifact_ready_claim_allowed": False,
            "external_delivery_claim_allowed": False,
            "stub_delivery": True,
            "stub_delivery_disclosure_required": True,
            "publish_allowed": False,
            "publish_reason": "validation_failed",
            "updated_at": now(),
        }
    )
    atts = out.get("attachments")
    if not isinstance(atts, list) or len(atts) == 0:
        out["attachments"] = [{"path": "report/rollback-stub.html", "sha256": ZERO64}]
    return out


def merge_rollback_final_answer_gate(fg: dict[str, Any]) -> dict[str, Any]:
    out: dict[str, Any] = dict(fg)
    out.update({"passed": False, "status": "fail", "updated_at": now()})
    return out
